// ARM-style SNR function
float arm_snr_f32(const float *pRef, const float *pTest, uint32_t buffSize) {
    float EnergySignal = 0.0f, EnergyError = 0.0f;
    uint32_t i;
    float SNR;

    for (i = 0; i < buffSize; i++) {
        EnergySignal += pRef[i] * pRef[i];
        EnergyError += (pRef[i] - pTest[i]) * (pRef[i] - pTest[i]);
    }

    if (EnergyError == 0.0f) {
        return 100.0f;
    }

    SNR = 10 * log10f(EnergySignal / EnergyError);
    return SNR;
}

// Function to reset cycle count (RISC-V processor)
void reset_cycle_count() {
    write_csr(NDS_MCYCLE, 0);
}

// Function to read cycle count (32-bit)
unsigned int read_cycle_count() {
    return read_csr(NDS_MCYCLE);
}

extern void user_init(void);

int main(void) {
    PLATFORM_INIT;
    CLOCK_INIT;
    user_init();

    printf("****************** \n");
    printf("FIR Program for RISC-V DSP Library\n");
	delay_ms(20);
    uint32_t i;
    riscv_fir_instance_f32 S;
    const float32_t *inputF32;
    float32_t *outputF32;

    /* Initialize input and output buffer pointers */
    inputF32 = &testInput_f32_1kHz_15kHz[0];
    outputF32 = &testOutput[0];

    /* Call FIR init function to initialize the instance structure. */
    riscv_fir_init_f32(&S, NUM_TAPS, (float32_t *)&firCoeffs32[0], &firStateF32[0], blockSize);

	// Reset cycle count
	reset_cycle_count();

    // Start cycle count
    uint32_t start_cycles = read_cycle_count();

    for (i = 0; i < numBlocks; i++) {
        riscv_fir_f32(&S, inputF32 + (i * blockSize), outputF32 + (i * blockSize), blockSize);
    }

    // End cycle count
    unsigned int end_cycles = read_cycle_count();

    // Calculate the total cycle count
    unsigned int cycle_count = end_cycles - start_cycles;

    printf("\nFIR Output vs. Reference Output:\n");
    delay_ms(20);
    for (i = 0; i < TEST_LENGTH_SAMPLES; i++) {
        printf("Index %3lu: %f,  %f\n", i, testOutput[i], refOutput[i]);
        delay_ms(20);
    }

    snr = arm_snr_f32(refOutput, testOutput, TEST_LENGTH_SAMPLES);
    printf("\nSNR: %f dB\n", snr);
    delay_ms(20);

    if (snr > 75.0f) {
        printf("SUCCESS: Filter output matches reference output.\n");
        delay_ms(20);
    } else {
        printf("FAILURE: SNR below threshold.\n");
        delay_ms(20);
    }
    printf("Cycle Count for D25F: %u\n", cycle_count);
    delay_ms(20);

    return 0;
}
